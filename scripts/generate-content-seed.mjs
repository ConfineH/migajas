import { readFileSync, writeFileSync } from "node:fs";
import { dirname, join } from "node:path";
import { fileURLToPath } from "node:url";

const root = join(dirname(fileURLToPath(import.meta.url)), "..");
const foods = JSON.parse(
  readFileSync(join(root, "src/lib/data/foods.json"), "utf8"),
);
const lessons = JSON.parse(
  readFileSync(join(root, "src/lib/data/lessons.json"), "utf8"),
);
const exams = JSON.parse(
  readFileSync(join(root, "src/lib/data/exams.json"), "utf8"),
);

function sqlString(value) {
  return `'${value.replace(/'/g, "''")}'`;
}

const foodRows = foods
  .map(
    (f) =>
      `(${sqlString(f.id)}, ${sqlString(f.country)}, ${sqlString(f.category)}, ${sqlString(f.name)}, ${sqlString(f.portionText)}, ${f.grams}, ${f.carbsG}, ${sqlString(f.difficulty)}, ${sqlString(f.itemType)}, ${sqlString(f.notes)})`,
  )
  .join(",\n");

const lessonRows = lessons
  .map(
    (l) =>
      `(${sqlString(l.id)}, ${sqlString(l.levelId)}, ${l.orderIndex}, ${sqlString(l.title)}, ${sqlString(l.summary)}, ${sqlString(JSON.stringify(l.steps))}::jsonb)`,
  )
  .join(",\n");

const examRows = exams
  .map((e) => {
    const pool = e.poolExerciseIds ?? e.exerciseIds ?? [];
    const count = e.questionsPerExam ?? (e.levelId === "nivel-5" ? 5 : 4);
    return `(${sqlString(e.levelId)}, ${sqlString(e.title)}, ${sqlString(e.description)}, ${sqlString(JSON.stringify(pool))}::jsonb, ${sqlString(JSON.stringify(pool))}::jsonb, ${count})`;
  })
  .join(",\n");

const sql = `-- generated by scripts/generate-content-seed.mjs
INSERT INTO public.foods (id, country, category, name, portion_text, grams, carbs_g, difficulty, item_type, notes) VALUES
${foodRows}
ON CONFLICT (id) DO UPDATE SET
  country = EXCLUDED.country,
  category = EXCLUDED.category,
  name = EXCLUDED.name,
  portion_text = EXCLUDED.portion_text,
  grams = EXCLUDED.grams,
  carbs_g = EXCLUDED.carbs_g,
  difficulty = EXCLUDED.difficulty,
  item_type = EXCLUDED.item_type,
  notes = EXCLUDED.notes,
  updated_at = now();

INSERT INTO public.lessons (id, level_id, order_index, title, summary, steps) VALUES
${lessonRows}
ON CONFLICT (id) DO UPDATE SET
  level_id = EXCLUDED.level_id,
  order_index = EXCLUDED.order_index,
  title = EXCLUDED.title,
  summary = EXCLUDED.summary,
  steps = EXCLUDED.steps,
  updated_at = now();

INSERT INTO public.level_exams (level_id, title, description, exercise_ids, pool_exercise_ids, questions_per_exam) VALUES
${examRows}
ON CONFLICT (level_id) DO UPDATE SET
  title = EXCLUDED.title,
  description = EXCLUDED.description,
  exercise_ids = EXCLUDED.exercise_ids,
  pool_exercise_ids = EXCLUDED.pool_exercise_ids,
  questions_per_exam = EXCLUDED.questions_per_exam,
  updated_at = now();
`;

const out = join(
  root,
  "supabase/migrations/20260713160000_seed_content.sql",
);
writeFileSync(out, sql);
console.log(`Wrote ${out}`);
